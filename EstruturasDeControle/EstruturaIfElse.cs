namespace Coder_CSharp.EstruturasDeControle;

public class EstruturaIfElse
{
    public static void Exercicios()
    {
        double nota = 7.0;

        if (nota >= 7.0)
        {
            Console.WriteLine("Aprovado!");
        }
        else
        {
            Console.WriteLine("Reprovado");
        }
    }
}